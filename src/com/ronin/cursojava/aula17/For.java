package com.ronin.cursojava.aula17;


public class For {

	public static void main(String[] args) {

			for (int i = 1; i <= 5; i++) {
				System.out.print(i + " - ");
			}
			System.out.println("FIM");
			
			System.out.println();
			
			for (int i = 5; i >= 1; i--) {
				System.out.print(i + " - ");
			}
			System.out.println("FIM");
			
			
	}

}
