package com.ronin.cursojava.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		int num1 = 4,
			num2 = 6;
		
		System.out.println(num1 + " > " + num2 + " = " + (num1 > num2));
		System.out.println(num1 + " < " + num2 + " = " + (num1 < num2));
		System.out.println(num1 + " >= " + num2 + " = " + (num1 >= num2));
		System.out.println(num1 + " <= " + num2 + " = " + (num1 <= num2));
		System.out.println(num1 + " != " + num2 + " = " + (num1 != num2));
		System.out.println(num1 + " = " + num2 + " = " + (num1  == num2));

	}

}
