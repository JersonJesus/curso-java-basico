package aula07;

public class OperadoresAtribuicao {
    
    public static void main(String[] args) {
        
        int num1 = 5;
        num1 += 2;
        System.out.println(5 + " += 2 = " + num1);
        
        num1 = 5;
        num1 -= 2;
        System.out.println(5 + " -= 2 = " + num1);
        
        num1 = 5;
        num1 *= 2;
        System.out.println(5 + " *= 2 = " + num1);
        
        num1 = 5;
        num1 /= 2;
        System.out.println(5 + " /= 2 = " + num1);
        
        num1 = 5;
        num1 %= 2;
        System.out.println(5 + " %= 2 = " + num1);
    }
}
